<script>
    document.addEventListener('DOMContentLoaded', () => {
        const section = document.querySelector('.envelope-cta');
        const letter = section.querySelector('.envelope-letter');

        /* When ~40 % of the section is visible, add .slide‑out */
        const io = new IntersectionObserver(
            (entries, obs) => {
                if (entries[0].isIntersecting) {
                    letter.classList.add('slide-out'); // triggers the new CSS above
                    obs.disconnect(); // fire only once
                }
            }, {
                threshold: 0.4
            }
        );

        io.observe(section);
    });
</script>





